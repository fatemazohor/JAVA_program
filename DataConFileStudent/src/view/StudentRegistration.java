    //student_register Column name
     String[] stuRegisterColumnName = {"Roll","Name","Age"};
    private void getAllStudentData(){
        sql="select * from student_register;";
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(stuRegisterColumnName);
        
        tabStudent.setModel(model);
        
        try {
            ps=dc.getCon().prepareStatement(sql);
            rs = ps.executeQuery();
            
            while (rs.next()) {
                int roll = rs.getInt("roll");
                String name = rs.getString("sname");
                int age = rs.getInt("age");
                
                model.addRow(new Object[]{roll,name,age});// directly writting stuRegisterColumnName cause all data to be array elements name.
                
            }
            ps.close();
            dc.getCon().close();
        } catch (SQLException ex) {
            Logger.getLogger(StudentRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    
    
    }

