    private void btnSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSubmitMouseClicked
        sql= "INSERT INTO student_register(sname,age) VALUES(?,?)";
        try {
            ps = dc.getCon().prepareStatement(sql);
            ps.setString(1, txtname.getText().trim());
            ps.setInt(2, Integer.parseInt(txtage.getText().trim()));
            ps.executeUpdate();
            
            ps.close();
            dc.getCon().close();
            JOptionPane.showMessageDialog(rootPane, "Data saved in student_register table");
            reset();
            getAllStudentData();
        } catch (SQLException ex) {
            Logger.getLogger(StudentRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_btnSubmitMouseClicked

