    private void btnsubmit1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnsubmit1MouseClicked
        // TODO add your handling code here:
        if (txtName.getText().trim().isEmpty()||txtSalary.getText().trim().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "All fields is requried. ");
            
        } else if(txtId.getText().trim().contains("Autogenerated")){
            
            String name = txtName.getText().trim();
            String designation = comboDesig.getSelectedItem().toString();
            String salary = txtSalary.getText().trim();
            
            sql = "insert into employee_table(name,designation,salary)values(?,?,?)";
            try {
                ps = db.getCon().prepareStatement(sql);
                ps.setString(1, name);
                ps.setString(2, designation);
                ps.setFloat(3, Float.parseFloat(salary));
                ps.executeUpdate();
                ps.close();
                db.getCon().close();
                JOptionPane.showMessageDialog(rootPane, "Data saved.");
                reset();
                getAlldata();
            } catch (SQLException ex) {
                Logger.getLogger(EmployeeTable.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "Data already exist in database. Please enter new data.");
        }
    }//GEN-LAST:event_btnsubmit1MouseClicked

