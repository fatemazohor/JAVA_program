    private void btnupdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnupdateMouseClicked
        // TODO add your handling code here:
        if (txtName.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "All fields is requried. ");
        } else {

            String id = txtId.getText();
            String name = txtName.getText().trim();
            String designation = comboDesig.getSelectedItem().toString();
            String salary = txtSalary.getText().trim();

            sql = "update employee_table set name=?,designation=?,salary=? where id=?";
            try {
                ps = db.getCon().prepareStatement(sql);
                ps.setString(1, name);
                ps.setString(2, designation);
                ps.setFloat(3, Float.parseFloat(salary));
                ps.setInt(4, Integer.parseInt(id));
                ps.executeUpdate();
                ps.close();
                db.getCon().close();
                JOptionPane.showMessageDialog(rootPane, "Data Updated.");
                reset();
                getAlldata();
            } catch (SQLException ex) {
                Logger.getLogger(EmployeeTable.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnupdateMouseClicked

